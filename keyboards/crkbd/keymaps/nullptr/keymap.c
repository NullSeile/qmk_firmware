bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case ND_GRV:
            if (record->event.pressed) {
                tap_code16(ES_GRV);
                tap_code(KC_SPC);
            }
            return false;
        case ND_CIRC:
            if (record->event.pressed) {
                tap_code16(ES_CIRC);
                tap_code(KC_SPC);
            }
            return false;
    }
    return true;
}

